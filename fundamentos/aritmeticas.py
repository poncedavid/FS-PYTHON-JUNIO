#Declarando las funciones
def sumar(a,b):
    suma = a+b
    return suma

def restar(a,b):
    resta = a-b
    return resta

def multiplicar(a,b):
    multiplicacion = a*b
    return multiplicacion

def dividir(a,b):
    division = a/b
    return division

def areaCuadrado(lado):
    area = lado*lado
    return area

def areaRectuangulo(base,altura):
    area = base*altura
    return area



